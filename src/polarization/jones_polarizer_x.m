function P = jones_polarizer_x(il,per)
% Jones matrix of polarizer // x
%
% -------------------------------------------------------------------------
% DESCRIPTION:
% -------------------------------------------------------------------------
% This function implements the Jones matrix of an imperfect polarizer 
% aligned to the -x axis of the canonical basis. 
%
% -------------------------------------------------------------------------
% FUNCTION CALL:
% -------------------------------------------------------------------------
% P = jones_polarizer_x(loss,extinction_ratio); 
%
% -------------------------------------------------------------------------
% INPUTS:
% -------------------------------------------------------------------------
% il                insertion loss of the polarizer, in dB [real scalar]
%
% per               polarization extinction ratio, in dB [real scalar]
%
%                       Use Inf for an ideal polarizer
%
% -------------------------------------------------------------------------
% OUTPUTS:
% -------------------------------------------------------------------------
% P                 Jones matrix of the polarizer [real 2x2 matrix]
%
% -------------------------------------------------------------------------
% GLOBAL:
% -------------------------------------------------------------------------
% 
%
% -------------------------------------------------------------------------
% REMARKS:
% -------------------------------------------------------------------------
% 
%
% -------------------------------------------------------------------------
% TO DO:
% -------------------------------------------------------------------------
% 
%
% -------------------------------------------------------------------------
% CREDITS:
% -------------------------------------------------------------------------
% 
%
% -------------------------------------------------------------------------
% AUTHOR:
% -------------------------------------------------------------------------
% Christophe Peucheret (christophe.peucheret@univ-rennes.fr)
%
% -------------------------------------------------------------------------
% -------------------------------------------------------------------------

t_max = 10^(-il/10);
% Maximum transmission in the -x orientation

epsilon = 10^(per/10);
% Convert extinction ratio to linear scale

P = sqrt(t_max)*[1 0; 0 1/sqrt(epsilon)];
% Calculate Jones matrix

end
% -------------------------------------------------------------------------
% End of function
% -------------------------------------------------------------------------